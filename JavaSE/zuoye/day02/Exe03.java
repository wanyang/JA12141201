/*
	3、创建Exe03.java：创建每一种数值型变量，赋值，进行各种“加减乘除”的运算
*/
public class Exe03{
	public static void main(String[] args){
		
		//byte类型
		byte a1 = 40,b1 = 2;
		System.out.println("byte类型" + "\na1+b1=" + (a1+b1) + "\na1-b1=" + (a1-b1) + "\na1*b1=" + (a1*b1) + "\na1/b1=" + (a1/b1));
		System.out.println("-------------");
		
		//short类型
		short a2 = 6, b2 = 3;
		System.out.println("short类型" + "\na2+b2=" + (a2+b2) + "\na2-b2=" + (a2-b2) + "\na2*b2=" + (a2*b2) + "\na2/b2=" + (a2/b2));
		System.out.println("-------------");
		
		//int类型
		int a3 = 8,b3 = 4;
		System.out.println("int类型" + "\na3+b3=" + (a3+b3) + "\na3-b3=" + (a3-b3) + "\na3*b3=" + (a3*b3) + "\na3/b3=" + (a3/b3));
		System.out.println("-------------");
		
		//long类型
		long a4 = 10,b4 = 5;
		System.out.println("long类型" + "\na4+b4=" + (a4+b4) + "\na4-b4=" + (a4-b4) + "\na4*b4=" + (a4*b4) + "\na4/b4=" + (a4/b4));
		System.out.println("-------------");
		
		float a5 = 12.4f,b5 = 3.1f;
		System.out.println("float类型" + "\na5+b5=" + (a5+b5) + "\na5-b5=" + (a5-b5) + "\na5*b5=" + (a5*b5) + "\na5/b5=" + (a5/b5));
		System.out.println("-------------");
		
		
		double a6 = 3.6,b6 = 0.6;
		System.out.println("double类型" + "\na6+b6=" + (a6+b6) + "\na6-b6=" + (a6-b6) + "\na6*b6=" + (a6*b6) + "\na6/b6=" + (a6/b6));
		System.out.println("-------------");
		
	}
}